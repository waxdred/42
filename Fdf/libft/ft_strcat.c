char	*ft_strcat(char *dest, char *s1)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (dest[i])
		i++;
	while (s1[j])
	{
		dest[i + j] = s1[j];
		j++;
	}
	dest[i + j] = '\0';
	return (dest);
}
